package com.strings;

public class StringCreation {

	public static void main(String[] args) {


		String s="india";//string as a datatype
		

		String m="india";
		
		System.out.println(s==m);


		String a=new String("india");
		String b=new String("india");
		
		System.out.println(a==b);
		
		

	}

}
