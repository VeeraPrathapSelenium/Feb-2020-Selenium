package com.tm.variables;

public class CheckDefaultValues {
	
	static int a;
	
	static String b;
	
	static float c;
	
	static double d;
	
	static long e;
	
	static boolean f;
	
	static short s;
	
	static char g;
	
	static byte j;
	
	
	
	

	public static void main(String[] args) {
		
		System.out.println("The default value for int "+a);
		
		System.out.println("The default value for String "+b);
		
		System.out.println("The default value for float "+c);
		
		System.out.println("The default value for double "+d);
		
		System.out.println("The default value for long "+e);
		
		System.out.println("The default value for boolean "+f);

		
		System.out.println("The default value for short "+s);
		
		System.out.println("The default value for char "+g);
		
		System.out.println("The default value for byte "+j);
	}

}
